[ORG 0x0100]

;INTITIALIZE

XOR AX , AX ;RESET THE ACCMULATOR
XOR BX , BX ;RESET THE BX REGISTER

LOOP:
    ADD AX , [NUM1 + BX]  
    ADD BX , 2  ; ADDING 2 EVRY TIME IN NUM1 (ADDRESS)

    CMP BX , 10    ; COMPARE THE BX CURRENT VALUE WITH 10 IF THEY PRODUCE ZERO THEN ZERO FLAG SET 
    JNE LOOP  ;IF ZERO FLAG IS NOT SET JUMP TO THE OUTERLOOP

    MOV [RESULT] ,AX  ;PUT THE LATEST AX VALUE IN RESULT MEMORY LOCATION


;EXIT

MOV AX , 0x4c00
INT 0x21 

;DATA

NUM1: DW 10 , 20, 30, 40, 50 
RESULT: DW 0 